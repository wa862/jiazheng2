-- 重新设计的订单表结构
CREATE TABLE IF NOT EXISTS orders (
    -- 基本信息
    order_id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '订单ID',
    order_no VARCHAR(32) NOT NULL UNIQUE COMMENT '订单编号',
    order_status VARCHAR(20) NOT NULL DEFAULT 'pending' COMMENT '订单状态：pending-待服务, processing-服务中, completed-已完成, cancelled-已取消',
    
    -- 用户信息
    user_id BIGINT NOT NULL COMMENT '用户ID',
    user_name VARCHAR(50) NOT NULL COMMENT '用户姓名',
    user_phone VARCHAR(20) NOT NULL COMMENT '用户电话',
    user_address VARCHAR(255) NOT NULL COMMENT '服务地址',
    
    -- 服务人员信息
    worker_id BIGINT NOT NULL COMMENT '服务人员ID',
    worker_name VARCHAR(50) NOT NULL COMMENT '服务人员姓名',
    worker_phone VARCHAR(20) NOT NULL COMMENT '服务人员电话',
    
    -- 服务信息
    service_type VARCHAR(50) NOT NULL COMMENT '服务类型：保洁服务, 月嫂服务, 护理服务, 钟点工, 育儿嫂, 老人陪护',
    service_date DATE NOT NULL COMMENT '服务日期',
    start_time TIME NOT NULL COMMENT '开始时间',
    end_time TIME NOT NULL COMMENT '结束时间',
    duration INT NOT NULL COMMENT '服务时长（分钟）',
    service_price DECIMAL(10, 2) NOT NULL COMMENT '服务单价（元/小时）',
    total_amount DECIMAL(10, 2) NOT NULL COMMENT '订单总金额',
    
    -- 支付信息
    payment_status VARCHAR(20) NOT NULL DEFAULT 'unpaid' COMMENT '支付状态：unpaid-未支付, paid-已支付, refunded-已退款',
    payment_method VARCHAR(20) COMMENT '支付方式：alipay-支付宝, wechat-微信, bank-银行转账, cash-现金',
    payment_time DATETIME COMMENT '支付时间',
    transaction_id VARCHAR(100) COMMENT '支付交易ID',
    
    -- 评价信息
    rating TINYINT COMMENT '评分（1-5星）',
    review_content TEXT COMMENT '评价内容',
    review_time DATETIME COMMENT '评价时间',
    
    -- 其他信息
    notes TEXT COMMENT '备注',
    cancel_reason TEXT COMMENT '取消原因',
    
    -- 时间戳
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    
    -- 索引
    INDEX idx_user_id (user_id),
    INDEX idx_worker_id (worker_id),
    INDEX idx_order_status (order_status),
    INDEX idx_service_date (service_date),
    INDEX idx_payment_status (payment_status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='订单表';

